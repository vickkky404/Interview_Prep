public class StrConcat {
    public static void main(String[] args) {
        String str1 = "Nalinikant";
        String str2 = "Bastia";
        String result = str1.concat(str2);
        String r2 = str1 + " " + str2;

        System.out.println(result);
        System.out.println(r2);
    }
}
